    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed

        SqlUsuarios modSql = new SqlUsuarios();
        usuarios mod = new usuarios();
        Date date = new Date();
        DateFormat fechaHora = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");

        String pass = new String(txtPassword.getPassword());

        if (!txtUsuario.getText().equals("") && !pass.equals("")) {
            String nuevoPass = hash.sha1(pass);
            mod.setUsuario(txtUsuario.getText());
            mod.setPassword(nuevoPass);
            mod.setLast_session(fechaHora.format(date));
            if (modSql.login(mod)) {

                if (mod.getId_tipo().equalsIgnoreCase("ADMINISTRADOR")) {
                    Admin mostrar = new Admin();
                    mostrar.setVisible(true);
                    this.setVisible(false);
                }

                if (mod.getId_tipo().equalsIgnoreCase("VENTANILLA")) {
                    Ventanilla mostrar = new Ventanilla();
                    mostrar.setVisible(true);
                    Ventanilla.txtUsuario.setText(txtUsuario.getText());
                    this.setVisible(false);
                }

                if (mod.getId_tipo().equalsIgnoreCase("RECEPCION")) {
                    Recepcion mostrar = new Recepcion();
                    mostrar.setVisible(true);
                    this.setVisible(false);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Datos incorrectos");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe ingresar sus datos");
        }
    }//GEN-LAST:event_btnIngresarActionPerformed
        
    
